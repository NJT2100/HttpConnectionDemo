    private void setIconFile(String filepath) {
        iconFile = new File(filepath);
    }
    
    private void setIcon() {
        ImageIcon icon = new ImageIcon(iconFile.getPath());
        int width = icon.getIconWidth();
        int height = icon.getIconHeight();
        float ratio = (float)imgLabel.getHeight() / icon.getIconHeight();
        Image image = icon.getImage().getScaledInstance(Math.round(width * ratio), Math.round(height * ratio), Image.SCALE_FAST);
        icon.setImage(image);
        imgLabel.setHorizontalAlignment(JLabel.CENTER);
        imgLabel.setIcon(icon);
    }
    
    private void setStatus(int code, String filename) {
        switch (code) 
        {
            case HttpURLConnection.HTTP_OK:
                statusLabel.setText("Successfully downloaded: " + filename);
            break;
            case HttpURLConnection.HTTP_ACCEPTED:
                statusLabel.setText("Successfully downloaded: " + filename);
            break;
            case HttpURLConnection.HTTP_BAD_REQUEST:
                statusLabel.setText("");
            break;
            case HttpURLConnection.HTTP_UNAUTHORIZED:
                statusLabel.setText("");
            break;
            case HttpURLConnection.HTTP_PAYMENT_REQUIRED:
                statusLabel.setText("");
            break;
            case HttpURLConnection.HTTP_FORBIDDEN:
                statusLabel.setText("");
            break;
            case HttpURLConnection.HTTP_NOT_FOUND:
                statusLabel.setText("");
            break;
        }
    }
    
