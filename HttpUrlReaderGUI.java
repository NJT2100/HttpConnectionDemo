    private void sendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendButtonActionPerformed
        if (urlField.getText().isEmpty())
            return;
        HttpUrlReader http = new HttpUrlReader();
        try {
            String filePath = "";
            JFileChooser save = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
            save.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
            save.setFileFilter(new FileNameExtensionFilter("Image Files", "png", "gif", "jpg"));

            int returnValue = save.showSaveDialog(null);
            if (returnValue == JFileChooser.APPROVE_OPTION) {
                if (save.getSelectedFile().isDirectory()) {
                    filePath = save.getSelectedFile().getPath();
                    String filename = urlField.getText().substring(
                        urlField.getText().lastIndexOf("/"));
                    filePath = filePath + filename;
                } else {
                    filePath = save.getSelectedFile().getPath();
                }
                int response = http.sendGet(urlField.getText(), filePath);
                setStatus(response, filePath);
                setIconFile(filePath);
                setIcon();
            }

        } catch (Exception ex) {
            Logger.getLogger(HttpUrlReaderGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_sendButtonActionPerformed

